// Reglas para los identificadores que definimos en nuestros propios programas:

// - No pueden contener dos guiones bajos consecutivos.
// - No pueden comenzar con un guión bajo seguido inmediatamente por una letra mayúscula.
// - Los identificadores definidos fuera de una función no comenzar con un guión bajo.

// Convenciones para nombres de variables:

//• Un identificador debe dar alguna indicación de su significado.
//• Los nombres de las variables normalmente están todo en minúsculas.
//exmple: alumno....no esta bien Alumno o ALUMNO.
//• Al igual que Sales_item, las clases que definimos generalmente comienzan con una letra mayúscula.
//• Los identificadores con varias palabras deben distinguir visualmente cada palabra.
// por ejemplo, nombre_alumno o nombreAlumno, no esta bien nombrealumno.

